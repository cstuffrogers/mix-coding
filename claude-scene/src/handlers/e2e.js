import { existsSync, readdirSync, readFileSync, writeFileSync, mkdirSync } from 'fs';
import { join } from 'path';
import chalk from 'chalk';
import { safeExec } from '../lib/safe-exec.js';

const ENDPOINT_DIRS = ['src/routes', 'pages/api', 'app/api', 'src/api', 'src/controllers'];
const SPEC_PATTERNS = ['openapi.json', 'openapi.yaml', 'swagger.json', 'swagger.yaml', 'api-spec.json', 'api-spec.yaml'];

function scanApiEndpoints(targetPath) {
  const endpoints = [];
  for (const dir of ENDPOINT_DIRS) {
    const full = join(targetPath, dir);
    if (!existsSync(full)) continue;
    try {
      const entries = readdirSync(full, { recursive: true });
      for (const entry of entries) {
        const ext = entry.split('.').pop();
        if (['ts', 'js', 'tsx', 'jsx'].includes(ext)) {
          const name = entry.replace(/\.[^.]+$/, '');
          const route = '/' + name.replace(/^index$/, '').replace(/\\/g, '/').replace(/\/route$/, '');
          endpoints.push({ dir, file: entry, route });
        }
      }
    } catch { /* skip */ }
  }
  return endpoints;
}

function findOpenApiSpec(targetPath) {
  for (const pattern of SPEC_PATTERNS) {
    const specPath = join(targetPath, pattern);
    if (existsSync(specPath)) return specPath;
    // also check nested locations
    const nestedPath = join(targetPath, 'docs', pattern);
    if (existsSync(nestedPath)) return nestedPath;
    const apiPath = join(targetPath, 'api', pattern);
    if (existsSync(apiPath)) return apiPath;
  }
  return '';
}

function generateMswHandlers(endpoints) {
  const routes = endpoints.map(ep => {
    const method = detectHttpMethod(ep.file);
    return `  http.${method}('${ep.route}', async () => {
    return HttpResponse.json({ message: '${ep.route} mock response' });
  });`;
  }).join('\n');

  return `// MSW request handlers — auto-generated by /e2e
import { http, HttpResponse } from 'msw';

export const handlers = [
${routes || '  // Add your handlers here: http.get(\'/api/users\', resolver)'}
];
`;
}

function generateMswServer() {
  return `// MSW server setup — auto-generated by /e2e
import { setupServer } from 'msw/node';
import { handlers } from './handlers';

export const server = setupServer(...handlers);
`;
}

function detectHttpMethod(filename) {
  const lower = filename.toLowerCase();
  if (lower.startsWith('post') || lower.includes('.post.')) return 'post';
  if (lower.startsWith('put') || lower.includes('.put.')) return 'put';
  if (lower.startsWith('delete') || lower.includes('.delete.')) return 'delete';
  if (lower.startsWith('patch') || lower.includes('.patch.')) return 'patch';
  return 'get';
}

function generateApiTest(endpoints, specPath) {
  const baseUrl = 'http://localhost:3000';
  const tests = endpoints.slice(0, 5).map(ep => {
    const method = detectHttpMethod(ep.file);
    return `
describe('${method.toUpperCase()} ${ep.route}', () => {
  it('returns 200 with valid response', async () => {
    const res = await request(app)
      .${method}('${ep.route}')
      .expect('Content-Type', /json/)
      .expect(200);
    expect(res.body).toBeDefined();
  });

  it('handles invalid input gracefully', async () => {
    const res = await request(app)
      .${method}('${ep.route}')
      .send({ invalid: true })
      .expect(res => res.status < 500);
  });
});`;
  }).join('\n');

  // Detect server entry
  const appImport = detectAppImport();

  return `// API integration tests — auto-generated by /e2e
import request from 'supertest';
${appImport}

${tests || `
describe('API Health', () => {
  it('responds to health check', async () => {
    const res = await request(app).get('/api/health');
    expect([200, 404]).toContain(res.status);
  });
});`}
${specPath ? `
// OpenAPI spec detected: run with:
// npx schemathesis run ${specPath} --base-url=${baseUrl}
` : ''}`;
}

function detectAppImport() {
  // Try common patterns for the server app export
  return `// TODO: Update this import to match your app's server entry point
// Example: import app from '../src/app';
// If using Next.js: const app = (await import('../src/app')).default;
// For now using a placeholder — replace with actual app import
const app = (() => {
  try {
    return require('../src/app');
  } catch {
    // Fallback: export a minimal Express-like app
    const express = require('express');
    return express();
  }
})();`;
}

function checkSupertestInstalled(targetPath) {
  const pkgPath = join(targetPath, 'package.json');
  if (!existsSync(pkgPath)) return { installed: false };
  try {
    const pkg = JSON.parse(readFileSync(pkgPath, 'utf-8'));
    const deps = { ...pkg.dependencies, ...pkg.devDependencies };
    return {
      installed: !!(deps.supertest),
      hasMsw: !!(deps.msw),
      hasSchemathesis: false, // schemathesis is a Python tool, run via npx or pip
    };
  } catch { return { installed: false }; }
}

function generateMswMocks(targetPath, endpoints) {
  try {
    const mocksDir = join(targetPath, 'src', 'mocks');
    if (!existsSync(mocksDir)) mkdirSync(mocksDir, { recursive: true });
    writeFileSync(join(mocksDir, 'handlers.js'), generateMswHandlers(endpoints), 'utf-8');
    writeFileSync(join(mocksDir, 'server.js'), generateMswServer(), 'utf-8');
    return true;
  } catch {
    return false;
  }
}

function generateIntegrationTests(targetPath, endpoints, specPath) {
  try {
    const testDir = join(targetPath, 'tests', 'integration');
    if (!existsSync(testDir)) mkdirSync(testDir, { recursive: true });
    writeFileSync(join(testDir, 'api.test.js'), generateApiTest(endpoints, specPath), 'utf-8');
    return true;
  } catch {
    return false;
  }
}

function runSchemathesisFuzz(targetPath, specPath) {
  try {
    const result = safeExec(`npx schemathesis run "${specPath}" --checks all --max-examples 50 2>&1 || true`, targetPath, { stdio: 'pipe' }).toString();
    if (result.includes('FAILED')) {
      return false;
    }
    return true;
  } catch {
    return false;
  }
}

function logE2eSetupInfo(endpoints, specPath, deps) {
  console.log(chalk.dim(`  API 端点: ${endpoints.length} 个`));
  if (specPath) console.log(chalk.dim(`  OpenAPI 规范: ${specPath}`));
  if (!deps.installed) console.log(chalk.yellow('  ⚠ Supertest 未安装，将生成测试骨架'));
  if (!deps.hasMsw) console.log(chalk.yellow('  ⚠ MSW 未安装，将生成 mock 骨架'));
}

export function handleSetupE2E(_action, _params, targetPath, context) {

  const endpoints = scanApiEndpoints(targetPath);
  const specPath = findOpenApiSpec(targetPath);
  const deps = checkSupertestInstalled(targetPath);

  logE2eSetupInfo(endpoints, specPath, deps);

  let isMswGenerated = false;
  let apiTestsGenerated;
  let isFuzzPassed = false;

  if (endpoints.length > 0) {
    isMswGenerated = generateMswMocks(targetPath, endpoints);
  } else {
    console.log(chalk.yellow('  ⚠ 未检测到 API 端点，跳过 MSW mock'));
  }

  apiTestsGenerated = generateIntegrationTests(targetPath, endpoints, specPath);

  if (specPath) {
    isFuzzPassed = runSchemathesisFuzz(targetPath, specPath);
  }

  if (context) {
    context.e2eConfigured = isMswGenerated || apiTestsGenerated;
    context.mswHandlersGenerated = isMswGenerated;
    context.apiTestsGenerated = apiTestsGenerated;
    context.fuzzTestPassed = isFuzzPassed;
    if (!context.e2eConfigured) context.lastStepFailed = true;
  }

  return context?.e2eConfigured
    ? `E2E 测试配置完成（MSW: ${isMswGenerated ? '是' : '否'}, API测试: ${apiTestsGenerated ? '是' : '否'}${isFuzzPassed ? ', Fuzz: 通过' : ''}）`
    : 'E2E 测试配置部分完成（检查警告）';
}
